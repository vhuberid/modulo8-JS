//Arrow Functions - forma de escrita da funcao para "economizar código"

const numberOne = 10
const numberTwo = 20

const sum = (numberOne, numberTwo) => numberOne + numberTwo      //com este formato já retorna automaticamente

console.log(`O primeiro número é ${numberOne}`)
console.log(`O segundo número é ${numberTwo}`)
console.log(`A soma dos dois números é ${sum (numberOne, numberTwo)}`)

